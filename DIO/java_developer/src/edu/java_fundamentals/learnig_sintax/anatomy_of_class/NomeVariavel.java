package edu.java_fundamentals.learnig_sintax.anatomy_of_class;

public class NomeVariavel {
    public static void main(String[] args) {
        //O uso de letras maiusculas em variaveis é uma convenção para o tipo de váriavel constante
        String BR = "Brasil";
        //Para garantir uma váriavel constante é utilizado uma palavra reservada: final
        final String CH = "China";
        //Por definição, uma variável simples pode ter ser  valor alterado conforme necessite
        int ano = 2024;
        ano = 2022;
        //Outros Exemplos:
        int TOTAL_ESTADOS_BRASILEIROS = 27;
        double PI = 3.14;
        String nomeAluno = "Roberto";

        //Declaração válida de variáveis
        int numero$um = 1;
        int numero1 = 1;
        int numeroum = 1;
        int longo = 1;
    }
}








